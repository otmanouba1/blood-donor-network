// Helper function to validate phone number for a country
export const validatePhoneForCountry = (phone, country) => {
  const pattern = PHONE_PATTERNS[country];
  if (!pattern) return "Phone validation not available for this country";
  
  if (Array.isArray(pattern.length)) {
    if (!pattern.length.includes(phone.length)) {
      return `Phone number must be ${pattern.length.join(' or ')} digits for ${country}`;
    }
  } else {
    if (phone.length !== pattern.length) {
      return `Phone number must be ${pattern.length} digits for ${country}`;
    }
  }
  
  if (!pattern.pattern.test(phone)) {
    return `Invalid phone number format for ${country}. Expected: ${pattern.format}`;
  }
  
  return "";
};

// Helper function to validate postal code for a country
export const validatePostalForCountry = (postal, country) => {
  const pattern = POSTAL_PATTERNS[country];
  if (!pattern) return "Postal code validation not available for this country";
  
  if (!pattern.pattern.test(postal)) {
    return `Invalid postal code format for ${country}. Expected: ${pattern.format}`;
  }
  
  return "";
};